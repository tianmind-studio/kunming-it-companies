import fs from "node:fs";
import {
  DIRECTION_BUCKETS,
  DISTRICT_BUCKETS,
  directionBucket,
  districtBucket,
  getCompanies,
  readDataset,
  stats
} from "./companies-lib.mjs";

const OUTPUT = "docs/data-quality-report.md";
const checkMode = process.argv.includes("--check");
const dataset = readDataset();
const companies = getCompanies(dataset);
const summary = stats(companies);

function countBy(items, fn, buckets = []) {
  const counts = new Map(buckets.map((bucket) => [bucket, 0]));
  for (const item of items) {
    const key = fn(item) || "unknown";
    counts.set(key, (counts.get(key) || 0) + 1);
  }
  return [...counts.entries()].filter(([, count]) => count > 0);
}

function table(headers, rows) {
  return [
    `| ${headers.join(" | ")} |`,
    `| ${headers.map(() => "---").join(" | ")} |`,
    ...rows.map((row) => `| ${row.join(" | ")} |`)
  ].join("\n");
}

function esc(value) {
  return String(value ?? "").replaceAll("|", "\\|").replace(/\s+/g, " ").trim();
}

const verificationRows = countBy(companies, (company) => company.verification_status, [
  "verified",
  "official_page",
  "community_pending",
  "outdated",
  "unknown"
]);
const directionRows = countBy(companies, directionBucket, DIRECTION_BUCKETS);
const districtRows = countBy(companies, districtBucket, DISTRICT_BUCKETS);

const missingDistrict = companies
  .filter((company) => districtBucket(company) === "待补区域")
  .slice(0, 20)
  .map((company) => [esc(company.name), esc(company.category), esc(company.verification_status), esc(company.source_url)]);

const weakSources = companies
  .filter((company) => Number(company.confidence_score || 0) <= 2)
  .slice(0, 20)
  .map((company) => [esc(company.name), esc(company.category), esc(company.source_type), esc(company.source_url)]);

const body = `# Data Quality Report / 数据质量报告

> Generated from \`data/companies.json\`. Do not edit this file by hand; run \`npm run generate:data-quality\`.

Last dataset update: \`${dataset.meta.updated_at}\`

## Summary

${table(["Metric", "Value"], [
  ["Companies / organizations", summary.total],
  ["Verified official websites", summary.verified],
  ["Verified official pages", summary.officialPage],
  ["Community pending records", summary.pending],
  ["Missing website", summary.missingWebsite],
  ["Direction buckets represented", summary.directionCount],
  ["District buckets represented", summary.districtCount]
])}

## Verification status

${table(["Status", "Count"], verificationRows)}

## Direction buckets

${table(["Direction", "Count"], directionRows)}

## District buckets

${table(["District", "Count"], districtRows)}

## Priority: missing district records

These records need district/county enrichment first because local geography is part of the value of the radar.

${missingDistrict.length ? table(["Name", "Category", "Status", "Source"], missingDistrict) : "No missing district records."}

## Priority: weak source records

These records currently rely on weak sources or low confidence. Upgrade them with official websites, official pages, or a second public source.

${weakSources.length ? table(["Name", "Category", "Source type", "Source"], weakSources) : "No weak source records."}

## Notes

- This report does not claim that any company is hiring.
- \`community_pending\` means the record needs stronger public sources, not that it is invalid.
- When updating data, keep the original source URL and the verification date.
`;

if (checkMode) {
  const existing = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, "utf8") : "";
  if (existing !== body) {
    console.error(`${OUTPUT} is out of date. Run npm run generate:data-quality.`);
    process.exit(1);
  }
  console.log(`${OUTPUT} is up to date.`);
} else {
  fs.writeFileSync(OUTPUT, body);
  console.log(`Generated ${OUTPUT}.`);
}
