import fs from "node:fs";
import {
  DIRECTION_BUCKETS,
  DISTRICT_BUCKETS,
  directionBucket,
  districtBucket,
  getCompanies,
  readDataset,
  stats
} from "./companies-lib.mjs";

const OUTPUT = "docs/data-quality-report.md";
const checkMode = process.argv.includes("--check");
const dataset = readDataset();
const companies = getCompanies(dataset);
const summary = stats(companies);

function countBy(items, fn, buckets = []) {
  const counts = new Map(buckets.map((bucket) => [bucket, 0]));
  for (const item of items) {
    const key = fn(item) || "unknown";
    counts.set(key, (counts.get(key) || 0) + 1);
  }
  return [...counts.entries()].filter(([, count]) => count > 0);
}

function table(headers, rows) {
  return [
    `| ${headers.join(" | ")} |`,
    `| ${headers.map(() => "---").join(" | ")} |`,
    ...rows.map((row) => `| ${row.join(" | ")} |`)
  ].join("\n");
}

function esc(value) {
  return String(value ?? "").replaceAll("|", "\\|").replace(/\s+/g, " ").trim();
}

const verificationRows = countBy(companies, (company) => company.verification_status, [
  "verified",
  "official_page",
  "community_pending",
  "outdated",
  "unknown"
]);
const directionRows = countBy(companies, directionBucket, DIRECTION_BUCKETS);
const districtRows = countBy(companies, districtBucket, DISTRICT_BUCKETS);
const sourceTypeRows = countBy(companies, (company) => company.source_type);
const checkedMonthRows = countBy(companies, (company) => {
  const date = company.last_checked || "unknown";
  return /^\d{4}-\d{2}-\d{2}$/.test(date) ? date.slice(0, 7) : "unknown";
});

const strongSourceCount = companies.filter((company) => ["verified", "official_page"].includes(company.verification_status)).length;
const missingSourceUrl = companies.filter((company) => !company.source_url).length;
const missingDistrictCount = companies.filter((company) => districtBucket(company) === "待补区域").length;
const weakSourceCount = companies.filter((company) => Number(company.confidence_score || 0) <= 2).length;
const latestChecked = companies
  .map((company) => company.last_checked)
  .filter(Boolean)
  .sort()
  .at(-1) || "unknown";

const missingDistrict = companies
  .filter((company) => districtBucket(company) === "待补区域")
  .sort((a, b) => a.name.localeCompare(b.name, "zh-CN"))
  .map((company) => [esc(company.name), esc(company.category), esc(company.verification_status), esc(company.source_type), esc(company.source_url)]);

const weakSources = companies
  .filter((company) => Number(company.confidence_score || 0) <= 2)
  .slice(0, 20)
  .map((company) => [esc(company.name), esc(company.category), esc(company.source_type), esc(company.source_url)]);

function priorityReason(company) {
  const reasons = [];
  if (company.verification_status === "community_pending") reasons.push("待复核");
  if (Number(company.confidence_score || 0) <= 2) reasons.push("弱来源");
  if (districtBucket(company) === "待补区域") reasons.push("缺区县");
  if (!company.website) reasons.push("缺官网");
  if ((company.opportunities || []).includes("unknown")) reasons.push("机会提示待判断");
  return reasons.join("、") || "常规复核";
}

function priorityScore(company) {
  let score = 0;
  if (company.verification_status === "community_pending") score += 3;
  if (Number(company.confidence_score || 0) <= 2) score += 3;
  if (districtBucket(company) === "待补区域") score += 2;
  if (!company.website) score += 1;
  if ((company.opportunities || []).includes("unknown")) score += 1;
  return score;
}

const priorityRecords = companies
  .map((company) => ({ company, score: priorityScore(company), reason: priorityReason(company) }))
  .filter((item) => item.score > 0)
  .sort((a, b) => b.score - a.score || a.company.name.localeCompare(b.company.name, "zh-CN"))
  .slice(0, 25)
  .map(({ company, score, reason }) => [
    esc(company.name),
    score,
    esc(reason),
    esc(company.verification_status),
    esc(company.source_type),
    esc(company.last_checked),
    esc(company.source_url)
  ]);

const body = `# Data Quality Report / 数据质量报告

> Generated from \`data/companies.json\`. Do not edit this file by hand; run \`npm run generate:data-quality\`.

Last dataset update: \`${dataset.meta.updated_at}\`
Latest company check date: \`${latestChecked}\`

## Summary

${table(["Metric", "Value"], [
  ["Companies / organizations", summary.total],
  ["Verified official websites", summary.verified],
  ["Verified official pages", summary.officialPage],
  ["Community pending records", summary.pending],
  ["Strong-source records", strongSourceCount],
  ["Strong-source ratio", summary.total ? `${Math.round((strongSourceCount / summary.total) * 100)}%` : "0%"],
  ["Missing website", summary.missingWebsite],
  ["Missing source_url", missingSourceUrl],
  ["Missing district", missingDistrictCount],
  ["Weak source records", weakSourceCount],
  ["Direction buckets represented", summary.directionCount],
  ["District buckets represented", summary.districtCount]
])}

## Verification status

${table(["Status", "Count"], verificationRows)}

## Direction buckets

${table(["Direction", "Count"], directionRows)}

## District buckets

${table(["District", "Count"], districtRows)}

## Source types

${table(["Source type", "Count"], sourceTypeRows)}

## Last checked distribution

${table(["Month", "Count"], checkedMonthRows)}

## Priority: records needing most attention

This list combines weak source, missing district, missing website, pending verification, and unknown opportunity hints. It is a review queue, not a quality ranking of companies.

${priorityRecords.length ? table(["Name", "Score", "Why", "Status", "Source type", "Last checked", "Source"], priorityRecords) : "No priority records."}

## 缺区县待补强列表 / Missing district review queue

These records need district/county enrichment first because local geography is part of the value of the radar. Do not infer district from company name alone; keep the field empty until a public source confirms it.

${missingDistrict.length ? table(["Name", "Category", "Status", "Source type", "Source"], missingDistrict) : "No missing district records."}

## Priority: weak source records

These records currently rely on weak sources or low confidence. Upgrade them with official websites, official pages, or a second public source.

${weakSources.length ? table(["Name", "Category", "Source type", "Source"], weakSources) : "No weak source records."}

## Notes

- This report does not claim that any company is hiring.
- \`community_pending\` means the record needs stronger public sources, not that it is invalid.
- Low confidence means the source is indirect or incomplete. It is not a negative judgment about the company.
- When updating data, keep the original source URL and the verification date.
`;

if (checkMode) {
  const existing = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, "utf8") : "";
  if (existing !== body) {
    console.error(`${OUTPUT} is out of date. Run npm run generate:data-quality.`);
    process.exit(1);
  }
  console.log(`${OUTPUT} is up to date.`);
} else {
  fs.writeFileSync(OUTPUT, body);
  console.log(`Generated ${OUTPUT}.`);
}
