import fs from "node:fs";
import {
  directionBucket,
  districtBucket,
  getCompanies,
  readDataset,
  sourceTypeLabel,
  verificationLabel
} from "./companies-lib.mjs";

const OUTPUT = "docs/data-cleanup-plan.md";
const checkMode = process.argv.includes("--check");
const dataset = readDataset();
const companies = getCompanies(dataset);

function esc(value) {
  return String(value ?? "").replaceAll("|", "\\|").replace(/\s+/g, " ").trim();
}

function table(headers, rows) {
  return [
    `| ${headers.join(" | ")} |`,
    `| ${headers.map(() => "---").join(" | ")} |`,
    ...rows.map((row) => `| ${row.map(esc).join(" | ")} |`)
  ].join("\n");
}

function gaps(company) {
  const items = [];
  if (company.verification_status === "community_pending") items.push("待复核");
  if (Number(company.confidence_score || 0) <= 2) items.push("弱来源");
  if (districtBucket(company) === "待补区域") items.push("缺区县");
  if (!company.website) items.push("缺官网");
  if ((company.opportunities || []).includes("unknown")) items.push("机会提示待判断");
  return items;
}

function reviewDirections(company) {
  const directions = new Set();

  if (!company.website) directions.add("官网");
  if (districtBucket(company) === "待补区域") directions.add("企查查/天眼查公开页");
  if (company.source_type === "government_public_list") {
    directions.add("政府项目页");
    directions.add("公众号文章");
  }
  if (company.source_type === "community_list") {
    directions.add("官网");
    directions.add("企查查/天眼查公开页");
    directions.add("公众号文章");
    directions.add("公开活动页");
  }
  if (company.source_type === "recruiting_platform") {
    directions.add("官网");
    directions.add("招聘页");
  }
  if (["public_web", "media_database", "unknown"].includes(company.source_type)) {
    directions.add("官网");
    directions.add("企查查/天眼查公开页");
    directions.add("公众号文章");
  }
  if (company.suitable_for_job_seekers || (company.opportunities || []).includes("hiring") || (company.opportunities || []).includes("internship")) {
    directions.add("招聘页");
  }
  if (!directions.size) directions.add("官网");

  return [...directions].join("、");
}

function priorityScore(company) {
  let score = 0;
  if (company.verification_status === "community_pending") score += 30;
  if (Number(company.confidence_score || 0) <= 2) score += 30;
  if (districtBucket(company) === "待补区域") score += 20;
  if (!company.website) score += 12;
  if ((company.opportunities || []).includes("unknown")) score += 8;
  return score;
}

const cleanupItems = companies
  .map((company) => ({
    company,
    score: priorityScore(company),
    gaps: gaps(company),
    reviewDirections: reviewDirections(company)
  }))
  .filter((item) => item.score > 0)
  .sort((a, b) => b.score - a.score || a.company.name.localeCompare(b.company.name, "zh-CN"));

const topRows = cleanupItems.slice(0, 40).map(({ company, score, gaps: itemGaps, reviewDirections: itemDirections }) => [
  company.name,
  score,
  itemGaps.join("、") || "常规复核",
  itemDirections,
  verificationLabel(company.verification_status),
  sourceTypeLabel(company.source_type),
  directionBucket(company),
  districtBucket(company),
  company.source_url
]);

const missingDistrictRows = companies
  .filter((company) => districtBucket(company) === "待补区域")
  .sort((a, b) => priorityScore(b) - priorityScore(a) || a.name.localeCompare(b.name, "zh-CN"))
  .map((company) => [
    company.name,
    verificationLabel(company.verification_status),
    sourceTypeLabel(company.source_type),
    directionBucket(company),
    reviewDirections(company),
    company.source_url
  ]);

const pendingRows = companies
  .filter((company) => company.verification_status === "community_pending")
  .sort((a, b) => priorityScore(b) - priorityScore(a) || a.name.localeCompare(b.name, "zh-CN"))
  .slice(0, 50)
  .map((company) => [
    company.name,
    priorityScore(company),
    gaps(company).join("、"),
    reviewDirections(company),
    sourceTypeLabel(company.source_type),
    company.source_url
  ]);

const weakRows = companies
  .filter((company) => Number(company.confidence_score || 0) <= 2)
  .sort((a, b) => priorityScore(b) - priorityScore(a) || a.name.localeCompare(b.name, "zh-CN"))
  .slice(0, 50)
  .map((company) => [
    company.name,
    company.confidence_score,
    gaps(company).join("、"),
    reviewDirections(company),
    sourceTypeLabel(company.source_type),
    company.source_url
  ]);

const body = `# Data Cleanup Plan / 数据清理与复核计划

> Generated from \`data/companies.json\`. Do not edit this file by hand; run \`npm run generate:data-quality\` or \`npm run generate:data-cleanup\`.

Dataset update: \`${dataset.meta.updated_at}\`

This plan lists records that can be improved without inventing facts. It does not add new companies, locations, hiring claims, outsourcing claims, or partnership claims. Every improvement should come from a public source.

## Current cleanup queue

| Queue | Count |
| --- | --- |
| Community pending records | ${companies.filter((company) => company.verification_status === "community_pending").length} |
| Weak source records | ${companies.filter((company) => Number(company.confidence_score || 0) <= 2).length} |
| Missing district records | ${missingDistrictRows.length} |
| Missing website records | ${companies.filter((company) => !company.website).length} |

## Top manual-review records

${table(["Name", "Priority", "Gaps", "Suggested review direction", "Status", "Source type", "Direction", "District", "Current source"], topRows)}

## 缺区县待补强列表

这些记录不能靠公司名猜区县。请优先通过官网“联系我们”页、公开工商资料页、政府项目页、公众号文章或招聘页确认。

${table(["Name", "Status", "Source type", "Direction", "Suggested review direction", "Current source"], missingDistrictRows)}

## Community pending records

${table(["Name", "Priority", "Gaps", "Suggested review direction", "Source type", "Current source"], pendingRows)}

## Weak source records

${table(["Name", "Confidence", "Gaps", "Suggested review direction", "Source type", "Current source"], weakRows)}

## How to use this plan

1. Open the current source first.
2. Look for stronger public sources only: 官网、企查查/天眼查公开页、政府项目页、公众号文章、招聘页、公开活动页。
3. If a stronger source is found, update \`data/companies.json\`, keep the old source in \`source_urls\` when useful, and update \`last_checked\`.
4. If no stronger source is found, keep the record conservative: \`community_pending\`, low confidence, empty district, or \`unknown\` opportunity hints.
5. Run \`npm run generate:companies\`, \`npm run export:csv\`, \`npm run generate:data-quality\`, and \`npm run validate\`.
`;

if (checkMode) {
  const existing = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, "utf8") : "";
  if (existing !== body) {
    console.error(`${OUTPUT} is out of date. Run npm run generate:data-cleanup.`);
    process.exit(1);
  }
  console.log(`${OUTPUT} is up to date.`);
} else {
  fs.writeFileSync(OUTPUT, body);
  console.log(`Generated ${OUTPUT}.`);
}
